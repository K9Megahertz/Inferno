#include "timer.h"
#include <iostream>
#include <iomanip>

namespace Inferno {

    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //
    //  CTORS / DTORS
    //
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    Timer::Timer(const std::string& name)
        : m_name(name), m_running(false)
    {
    }


    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //
    //  Function start
    //
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    void Timer::start()
    {
        m_laps.clear();
        m_running = true;
        m_start = Clock::now();
    }


    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //
    //  Function lap
    //
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    void Timer::lap(const std::string& label)
    {
        if (!m_running) {
            return;
        }

        m_laps.push_back({ label, Clock::now() });
    }


    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //
    //  Function stop
    //
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    void Timer::stop()
    {
        if (!m_running) {
            return;
        }

        m_end = Clock::now();
        m_running = false;

        TimePoint prev = m_start;

        //std::cout << "Timer [" << m_name << "]\n";

        for (const auto& lap : m_laps) {
            std::chrono::duration<double, std::milli> segment = lap.time - prev;

            std::cout
                << "  "
                << lap.label
                << ": "
                << std::fixed
                << std::setprecision(3)
                << segment.count()
                << " ms\n";

            prev = lap.time;
        }

        std::chrono::duration<double, std::milli> final_segment = m_end - prev;
        std::chrono::duration<double, std::milli> total = m_end - m_start;

        /*std::cout
            << "  stop: "
            << std::fixed
            << std::setprecision(3)
            << final_segment.count()
            << " ms\n";

        std::cout
            << "  total: "
            << std::fixed
            << std::setprecision(3)
            << total.count()
            << " ms\n";*/
    }


    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //
    //  Function elapsed_ms
    //
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    double Timer::elapsed_ms() const
    {
        if (!m_running) {
            std::chrono::duration<double, std::milli> duration = m_end - m_start;
            return duration.count();
        }

        auto now = Clock::now();
        std::chrono::duration<double, std::milli> duration = now - m_start;
        return duration.count();
    }


    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //
    //  Function elapsed_sec
    //
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    double Timer::elapsed_sec() const
    {
        if (!m_running) {
            std::chrono::duration<double> duration = m_end - m_start;
            return duration.count();
        }

        auto now = Clock::now();
        std::chrono::duration<double> duration = now - m_start;
        return duration.count();
    }


    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //
    //  Function is_running
    //
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    bool Timer::is_running() const
    {
        return m_running;
    }


    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //
    //  Function reset
    //
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    void Timer::reset()
    {
        m_running = false;
        m_laps.clear();
    }

}