#pragma once
#include <vector>
#include <inferno/core/tensor.h>



namespace Inferno {



    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //
    //  Class OptimizerSGD
    //
    //
    //
    //
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


    class OptimizerSGD {


        

    public:


        //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        //
        //  CTORS // DTORS
        //
        //
        //
        //
        //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        OptimizerSGD(const std::vector<Tensor*>& parameters, float learning_rate);



        //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        //
        //  Function cpu_step_impl
        //
        //
        //
        //
        //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        template <typename AT, typename BT>
        void cpu_step_impl(AT* dptr, BT* gptr, size_t N) {
            for (size_t i = 0; i < N; i++) {
                dptr[i] = static_cast<AT>(static_cast<double>(dptr[i]) - static_cast<double>(m_lr) * static_cast<double>(gptr[i]));
            }
        }

        void step();
        void zero_grad();

    private:



        std::vector<Tensor*> m_params;
        float m_lr;


      
    };

}
