	SoftmaxBackward::SoftmaxBackward(const Tensor& A, const Tensor& out, int axis) : m_A(A), m_out(out), m_axis(axis) {
		set_name("SoftmaxBackward");
	}


	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//
	//  Function backward
	//
	//
	//
	//
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	void SoftmaxBackward::backward() {

		NoGradGuard guard;
		Tensor g_out = Engine::grad_in(this, 0);

		Tensor g_a = dispatchFloatTwo(m_out.dtype(), g_out.dtype(), [&](auto TA, auto TG) {
			using AT = typename decltype(TA)::type;
			using GT = typename decltype(TG)::type;
			using RT = promote_t<AT, GT>;




			Inferno::Tensor out(dtype_of_v<RT>, m_A.shape(), "SoftmaxBackward", m_A.device());


			//get pointers to data
			AT* aptr = GetImpl(m_out)->data_as_ptr<AT>();
			GT* gptr = GetImpl(g_out)->data_as_ptr<GT>();
			RT* optr = GetImpl(out)->data_as_ptr<RT>();

			const auto& shape = m_out.shape();
			const auto& ystrides = m_out.strides();
			const auto& gstrides = g_out.strides();
			const auto& ostrides = out.strides();

			const size_t yoffset = GetImpl(m_out)->offset();
			const size_t goffset = GetImpl(g_out)->offset();
			const size_t ooffset = GetImpl(out)->offset();



			switch (g_out.device().m_type) {

				////////////////////////////////////////////////////
				// CPU Code Path
				////////////////////////////////////////////////////
			case DeviceType::CPU:
				Logger::Append(Logger::LogLevel::LOGLEVEL_DEBUG, "CPU Code path - Using normal softmax backward path");
				cpu_softmax_backward<AT, GT, RT>(
					aptr,
					gptr,
					optr,
					shape,
					ystrides,
					gstrides,
					ostrides,
					yoffset,
					goffset,
					ooffset,
					m_axis
				);
				break;

				////////////////////////////////////////////////////
				// CUDA Code Path
				////////////////////////////////////////////////////
			case DeviceType::CUDA:
				Logger::Append(Logger::LogLevel::LOGLEVEL_DEBUG, "CUDA Code path - Using normal softmax backward path");
				cuda_softmax_backward<AT, GT, RT>(
					aptr,
					gptr,
					optr,
					shape,
					ystrides,
					gstrides,
					ostrides,
					yoffset,
					goffset,
					ooffset,
					m_axis
				);
				break;

			default:
				Logger::Append(Logger::LogLevel::LOGLEVEL_ERROR, "Invalid device type");
				exit(1);
			}

			return out;

			});


		// find parent nodes
		auto na = GetImpl(m_A)->grad_edge();


		// send gradients upstream
		if (na)
			Engine::accumulate(na.get(), 0, g_a);

	}


	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//
	//  Function release
	//
	//
	//
	//
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	void SoftmaxBackward::release() {
		// drop references so graph can free
		m_A = Tensor{};
		m_out = Tensor{};

	}


	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//
	//  Function get_inputs
	//
	//
	//
	//
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	void SoftmaxBackward::get_inputs(std::vector<Tensor>& out) const {
		out.push_back(m_A);

	}


