#pragma once
#include <memory>
#include <vector>
#include <cstddef>

namespace Inferno {

	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//
	//  Class Storage
	// 
	//
	//
	//
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	class Storage {

	public:

		Storage() { m_numbytes = 0; };
		~Storage() {};


		size_t nbytes() {
			return m_numbytes;
		}

		//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
		//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
		//
		//  Function storage_as_ptr
		//
		//
		//
		//
		//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
		//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

		template<typename T>
		T* storage_as_ptr() {
			void* ptr = raw_ptr();
			return static_cast<T*>(ptr);
		}

		template<typename T>
		T* storage_as_ptr() const {
			void* ptr = raw_ptr();
			return static_cast<T*>(ptr);
		}


		virtual void* raw_ptr() = 0;


	protected:
		size_t m_numbytes;
		



	};


}