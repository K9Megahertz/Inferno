	void Module::to(const Device& device) {
		for (auto& [name, param] : _parameters) {
			if (param) {
				*param = param->to(device);
			}
		}

		for (auto& [name, buf] : _buffers) {
			if (buf) {
				*buf = buf->to(device);
			}
		}

		for (auto& [name, child] : _children) {
			if (child) {
				child->to(device);
			}
		}
	}


