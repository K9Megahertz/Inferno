	std::vector<Tensor*> Module::parameters() const {
		std::vector<Tensor*> all_params;



		// add this module's own parameters
		for (const auto& [name, tensor_ptr] : _parameters) {
			all_params.push_back(tensor_ptr);
		}

		// recurse into child modules
		for (const auto& [name, child] : _children) {
			auto child_params = child->parameters();
			all_params.insert(all_params.end(), child_params.begin(), child_params.end());
		}

		return all_params;
	}

	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//
	//  Function register_parameter
	//
	//
	//
	//
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	void Module::register_parameter(const std::string& name, Tensor* tensor) {
		// Check for name collisions
		if (_parameters.count(name)) {
			throw std::runtime_error("Parameter already registered: " + name);
		}
		if (_buffers.count(name)) {
			throw std::runtime_error("Name already used by buffer: " + name);
		}
		if (_children.count(name)) {
			throw std::runtime_error("Name already used by submodule: " + name);
		}

		// Store pointer to the tensor
		_parameters[name] = tensor;
	}


