#pragma once
#include <unordered_map>
#include "../GradFN/node.h"
#include <inferno/core/tensor.h>
#include <inferno/core/ops.h>
#include "inferno/core/tensorimpl.h"

namespace Inferno {

    extern bool grad_enabled;

    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //
    //  NoGradGuard 
    //
    //  RAII helper used to temporarily disable gradient tracking.
    //
    //  When this object is constructed:
    //      - Saves the current global grad_enabled state
    //      - Sets grad_enabled = false
    //
    //  When it goes out of scope (destructor):
    //      - Restores the previous grad_enabled state
    //
    //  Used inside backward passes or internal ops where we do NOT want to build
    //  additional computation graph nodes.
    //
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    class NoGradGuard {
    public:
        NoGradGuard() {
            m_prev = Inferno::grad_enabled;
            Inferno::grad_enabled = false;
        }

        ~NoGradGuard() {
            Inferno::grad_enabled = m_prev;
        }

    private:
        bool m_prev;
    };


    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //
    //  Edge 
    //
    //  Represents a connection in the autograd graph.
    //
    //  Each edge identifies:
    //      - A Node (operation in the graph)
    //      - A specific output slot of that node
    //
    //  This allows handling:
    //      - Nodes with multiple outputs
    //      - Correct gradient routing per output
    //
    //  Used as a key into the gradient map.
    //
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    struct Edge {
        Node* node;
        int slot;

        bool operator==(const Edge& other) const {
            return node == other.node && slot == other.slot;
        }
    };

    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //
    //  EdgeHash 
    //
    //  Custom hash function for Edge so it can be used as a key in unordered_map.
    //
    //  Combines:
    //      - Node pointer hash
    //      - Slot index hash
    //
    //  Used for the gradient map inside Engine.
    //
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    struct EdgeHash {
        std::size_t operator()(const Edge& e) const {
            return std::hash<Node*>()(e.node) ^ (std::hash<int>()(e.slot) << 1);
        }
    };


    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //
    //  Engine 
    //
    //  Core autograd execution engine.
    //
    //  Responsibilities:
    //      - Executes backward pass
    //      - Builds topological ordering of computation graph
    //      - Stores intermediate gradients
    //      - Routes gradients between nodes
    //
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    class Engine {

    public:

        //////////////////////////////////////////////////////////////////////////////////////////////////////
        // backward
        //
        // Entry point for backpropagation.
        //
        // Takes a final output tensor (typically a loss) and:
        //      - Builds the computation graph (topological order)
        //      - Seeds initial gradient (usually ones_like)
        //      - Traverses nodes in reverse order
        //      - Calls each node's backward() function
        //
        //////////////////////////////////////////////////////////////////////////////////////////////////////
        static void backward(const Tensor& tensor);

        //////////////////////////////////////////////////////////////////////////////////////////////////////
        // accumulate
        //
        // Adds a gradient contribution to a node's input slot.
        //
        // Used when multiple downstream paths contribute gradients to the same node.
        //
        // Example:
        //      If two branches feed into a node, their gradients are summed here.
        //
        //////////////////////////////////////////////////////////////////////////////////////////////////////
        static void accumulate(Node* node, int slot, Tensor& grad);

        //////////////////////////////////////////////////////////////////////////////////////////////////////
        // grad_in
        //
        // Retrieves the accumulated gradient for a given node and slot.
        //
        // This is what each Node::backward() uses as its upstream gradient.
        //
        //////////////////////////////////////////////////////////////////////////////////////////////////////
        static Tensor grad_in(Node* node, int slot);

        //////////////////////////////////////////////////////////////////////////////////////////////////////
        // build_topo
        //
        // Recursively builds a topologically sorted list of nodes in the graph.
        //
        // Ensures:
        //      - Each node is visited once
        //      - Dependencies are processed before dependents
        //
        // Used to determine correct backward execution order.
        //
        //////////////////////////////////////////////////////////////////////////////////////////////////////
        static void build_topo(std::shared_ptr<Node> node, std::unordered_set<Node*>& visited, std::vector<Node*>& topo);

    private:

        //////////////////////////////////////////////////////////////////////////////////////////////////////
        // s_grad_map
        //
        // Thread-local map storing accumulated gradients.
        //
        // Key:
        //      Edge (node + slot)
        //
        // Value:
        //      Tensor representing accumulated gradient for that edge
        //
        // Thread-local ensures:
        //      - Safe usage in multi-threaded environments
        //      - Each thread has its own gradient state
        //
        //////////////////////////////////////////////////////////////////////////////////////////////////////
        static thread_local std::unordered_map<Edge, Tensor, EdgeHash>* s_grad_map;

    };

}