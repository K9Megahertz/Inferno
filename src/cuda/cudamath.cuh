#pragma once

#include <type_traits>
#include <cuda_runtime.h>
#include <device_functions.h>
#include <math.h>

namespace Inferno {

	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//
	//  Function cuda_sqrt
	//
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	template<typename T>
	__device__ __forceinline__ T cuda_sqrt(T x) {
		if constexpr (std::is_same_v<T, int>) {
			return static_cast<int>(sqrtf(static_cast<float>(x)));
		}
		else if constexpr (std::is_same_v<T, float>) {
			return sqrtf(x);
		}
		else if constexpr (std::is_same_v<T, double>) {
			return sqrt(x);
		}
		else {
			static_assert(
				std::is_same_v<T, int> ||
				std::is_same_v<T, float> ||
				std::is_same_v<T, double>,
				"cuda_sqrt only supports int, float, and double"
				);
		}
	}


	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//
	//  Function cuda_tanh
	//
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	template<typename T>
	__device__ __forceinline__ T cuda_tanh(T x) {
		if constexpr (std::is_same_v<T, int>) {
			return static_cast<int>(sqrtf(static_cast<float>(x)));
		}
		else if constexpr (std::is_same_v<T, float>) {
			return tanhf(x);
		}
		else if constexpr (std::is_same_v<T, double>) {
			return tanh(x);
		}
		else {
			static_assert(
				std::is_same_v<T, int> ||
				std::is_same_v<T, float> ||
				std::is_same_v<T, double>,
				"cuda_tanh only supports int, float and double"
				);
		}
	}

}