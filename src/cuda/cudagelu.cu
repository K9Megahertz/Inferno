	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//
	//  Function gelu_tanh_approx_cuda
	//
	//
	//
	//
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	template<typename T>
	__device__ inline T gelu_tanh_approx_cuda(T x) {
		const T kAlpha = static_cast<T>(0.7978845608028654); // sqrt(2/pi)
		const T kBeta = static_cast<T>(0.044715);

		T x3 = x * x * x;
		T inner = kAlpha * (x + kBeta * x3);
		return static_cast<T>(0.5) * x * (static_cast<T>(1) + cuda_tanh(inner));
	}


	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//
	//  Function gelu_kernel
	//
	//
	//
	//
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	template<typename AT, typename RT>
	__global__ void gelu_kernel(const AT* aptr,	RT* optr, size_t N, size_t off) {

		size_t idx = static_cast<size_t>(blockIdx.x) * blockDim.x + threadIdx.x;
		if (idx >= N) return;

		RT x = static_cast<RT>(aptr[off + idx]);
		optr[idx] = gelu_tanh_approx_cuda<RT>(x);
	}


	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//
	//  Function cuda_gelu
	//
	//
	//
	//
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	template<typename AT, typename RT>
	void cuda_gelu(const AT* aptr, RT* optr, size_t N, size_t off) {
		constexpr int threads = 256;
		int blocks = static_cast<int>((N + threads - 1) / threads);

		gelu_kernel<AT, RT> << <blocks, threads >> > (aptr, optr, N, off);

		cudaError_t err = cudaGetLastError();
		if (err != cudaSuccess) {
			std::printf("cuda_gelu launch failed: %s\n", cudaGetErrorString(err));
		}
	}


