	template<typename AT, typename RT>
	__global__ void gelu_strided_kernel(
		const AT* aptr,
		RT* optr,
		const size_t* shape,
		const size_t* astrides,
		const size_t* ostrides,
		int ndim,
		size_t aoffset,
		size_t ooffset,
		size_t N
	) {
		size_t linear = static_cast<size_t>(blockIdx.x) * blockDim.x + threadIdx.x;
		if (linear >= N) return;

		size_t tmp = linear;
		size_t aidx = aoffset;
		size_t oidx = ooffset;

		// unravel linear index into coords, then apply strides
		for (int d = ndim - 1; d >= 0; --d) {
			size_t coord = tmp % shape[d];
			tmp /= shape[d];

			aidx += coord * astrides[d];
			oidx += coord * ostrides[d];
		}

		RT x = static_cast<RT>(aptr[aidx]);
		optr[oidx] = gelu_tanh_approx_cuda<RT>(x);
	}

	template<typename AT, typename RT>
	void cuda_gelu_strided(
		const AT* aptr,
		RT* optr,
		const std::vector<size_t>& shape,
		const std::vector<size_t>& astrides,
		const std::vector<size_t>& ostrides,
		size_t aoffset,
		size_t ooffset
	) {
		const int ndim = static_cast<int>(shape.size());

		if (astrides.size() != shape.size() || ostrides.size() != shape.size()) {
			throw std::runtime_error("cuda_gelu_strided: shape/stride rank mismatch");
		}

		size_t N = 1;
		for (size_t d = 0; d < shape.size(); ++d) {
			N *= shape[d];
		}

		size_t* d_shape = nullptr;
		size_t* d_astrides = nullptr;
		size_t* d_ostrides = nullptr;

		cudaMalloc(&d_shape, ndim * sizeof(size_t));
		cudaMalloc(&d_astrides, ndim * sizeof(size_t));
		cudaMalloc(&d_ostrides, ndim * sizeof(size_t));

		cudaMemcpy(d_shape, shape.data(), ndim * sizeof(size_t), cudaMemcpyHostToDevice);
		cudaMemcpy(d_astrides, astrides.data(), ndim * sizeof(size_t), cudaMemcpyHostToDevice);
		cudaMemcpy(d_ostrides, ostrides.data(), ndim * sizeof(size_t), cudaMemcpyHostToDevice);

		constexpr int threads = 256;
		int blocks = static_cast<int>((N + threads - 1) / threads);

		gelu_strided_kernel<AT, RT> << <blocks, threads >> > (
			aptr,
			optr,
			d_shape,
			d_astrides,
			d_ostrides,
			ndim,
			aoffset,
			ooffset,
			N
			);

		cudaError_t err = cudaGetLastError();
		if (err != cudaSuccess) {
			std::printf("cuda_gelu_strided launch failed: %s\n", cudaGetErrorString(err));
		}

		cudaFree(d_shape);
		cudaFree(d_astrides);
		cudaFree(d_ostrides);
	}


