	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//
	//  Function gelu_grad_tanh_approx_cuda
	//
	//
	//
	//
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


	template<typename T>
	__device__ __forceinline__ T gelu_grad_tanh_approx_cuda(T x) {
		const T kAlpha = static_cast<T>(0.7978845608028654); // sqrt(2/pi)
		const T kBeta = static_cast<T>(0.044715);
		const T kThreeBeta = static_cast<T>(3.0) * kBeta;
		const T kHalf = static_cast<T>(0.5);
		const T kOne = static_cast<T>(1.0);

		T x2 = x * x;
		T x3 = x2 * x;

		T u = kAlpha * (x + kBeta * x3);
		T t = cuda_tanh(u);
		T sech2 = kOne - t * t;

		return kHalf * (kOne + t)
			+ kHalf * x * sech2 * kAlpha * (kOne + kThreeBeta * x2);
	}


	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//
	//  Function gelu_backward_kernel
	//
	//
	//
	//
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	template<typename AT, typename GT, typename RT>
	__global__ void gelu_backward_kernel(
		const AT* aptr,
		const GT* gptr,
		RT* optr,
		size_t N,
		size_t off
	) {
		size_t idx = static_cast<size_t>(blockIdx.x) * blockDim.x + threadIdx.x;
		if (idx >= N) return;

		RT x = static_cast<RT>(aptr[off + idx]);
		RT g = static_cast<RT>(gptr[idx]);

		RT dgelu = gelu_grad_tanh_approx_cuda<RT>(x);
		optr[idx] = g * dgelu;
	}


	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//
	//  Function cuda_gelu_backward
	//
	//
	//
	//
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	template<typename AT, typename GT, typename RT>
	void cuda_gelu_backward(const AT* aptr, const GT* gptr, RT* optr, size_t N, size_t off) {

		constexpr int threads = 256;
		int blocks = static_cast<int>((N + threads - 1) / threads);

		gelu_backward_kernel<AT, GT, RT> << <blocks, threads >> > (aptr, gptr, optr, N, off);

		cudaError_t err = cudaGetLastError();
		if (err != cudaSuccess) {
			std::printf("cuda_gelu_backward launch failed: %s\n", cudaGetErrorString(err));
		}
	}

