	//TensorImpl(DType type, const void* data, const std::vector<size_t> shape, const std::string name, Inferno::Device device, bool requires_grad = false);
	
	TensorImpl::TensorImpl(DType type, const void* data, const std::vector<size_t> shape, const std::string name, Inferno::Device device, bool requires_grad) {
		m_device = device;
		m_dtype = type;
		m_shape = shape;
		m_offset = 0;
		m_name = name;
		m_strides = calculate_strides(shape);
		m_grad = nullptr;
		m_requires_grad = requires_grad;
		m_isview = false;
		m_grad_fn = nullptr;
		m_grad_accum = nullptr;

		//TODO: validate T == dtype		
		size_t bytes = numel() * dtype_size(type);

		if (device.m_type == DeviceType::CPU) {
			m_data = std::make_shared<CPUStorage>(bytes);
			std::memcpy(m_data->raw_ptr(), data, bytes);
		}
		else if (device.m_type == DeviceType::CUDA) {
			m_data = std::make_shared<CUDAStorage>(bytes);
			cudaError_t err = cudaMemcpy(m_data->raw_ptr(), data, bytes, cudaMemcpyHostToDevice);
			if (err != cudaSuccess) {
				Logger::Append(Logger::LogLevel::LOGLEVEL_ERROR, "Could not cudaMemcpy in TensorImpl constructor.");
				exit(1);
			}
		}
		else {
			Logger::Append(Logger::LogLevel::LOGLEVEL_ERROR, "Attempt to create TensorImpl with unknown device type.");
			exit(1);
		}

		m_id = Inferno::IDBroker::GenID();
		Inferno::NodeTracker::addID(this->m_id, this->m_name);


	}


