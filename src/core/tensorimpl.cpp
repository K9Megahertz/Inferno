	//
	//  Function is_contiguous
	//
	//
	//
	//
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	bool TensorImpl::is_contiguous() const {

		if (m_shape.empty())
			return true;

		size_t expected = 1;

		for (int d = static_cast<int>(m_shape.size()) - 1; d >= 0; --d) {

			if (m_strides[d] != expected)
				return false;

			expected *= m_shape[d];
		}

		return true;
	}

