	// Calculate strides based on shape	
	std::vector<size_t> TensorImpl::calculate_strides(std::vector<size_t> shape) {
		std::vector<size_t> strides(shape.size());

		size_t stride = 1;
		for (int i = (int)shape.size() - 1; i >= 0; --i) {
			strides[i] = stride;
			stride *= shape[i];
		}

		return strides;
	}


