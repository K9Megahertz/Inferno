	//
	//  Function calculate_strides
	//
	//
	//
	//
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	std::vector<size_t> Tensor::calculate_strides(const std::vector<size_t>& shape) {
		std::vector<size_t> strides(shape.size());
		size_t stride = 1;
		for (int i = shape.size() - 1; i >= 0; i--) {
			strides[i] = stride;
			stride *= shape[i];
		}
		return strides;

	}


