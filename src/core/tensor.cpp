	std::vector<size_t> Tensor::get_broadcast_shape(std::vector<size_t> a, std::vector<size_t> b) {

		size_t a_ndim = a.size();
		size_t b_ndim = b.size();
		size_t result_ndim = std::max(a_ndim, b_ndim);

		std::vector<size_t> result_shape(result_ndim);

		// Handle broadcasting from right to left
		for (int i = 1; i <= result_ndim; i++) {
			size_t a_dim = (i <= a_ndim) ? a[a_ndim - i] : 1;
			size_t b_dim = (i <= b_ndim) ? b[b_ndim - i] : 1;

			// Broadcasting rules: dimensions must be equal or one of them must be 1
			if (a_dim != b_dim && a_dim != 1 && b_dim != 1) {
				throw std::runtime_error("Incompatible shapes for broadcasting");
			}

			result_shape[result_ndim - i] = std::max(a_dim, b_dim);
		}

		return result_shape;

	}

