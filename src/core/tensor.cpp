	Tensor Tensor::ones(const std::vector<size_t>& shape,DType dtype,Device device,const std::string& name) {
		
		Tensor out(dtype, shape, name, device);

		size_t numel = out.numel();

		dispatchAny(dtype, [&](auto TagT) {
			using AT = typename decltype(TagT)::type;

			AT* optr = GetImpl(out)->data_as_ptr<AT>();

			switch (out.device().m_type) {

				////////////////////////////////////////////////////
				// CPU Code Path
				////////////////////////////////////////////////////
			case DeviceType::CPU:
				Logger::Append(Logger::LogLevel::LOGLEVEL_DEBUG, "CPU Code path - Using normal ones path");
				for (size_t i = 0; i < numel; i++)
					optr[i] = static_cast<AT>(1);
				break;

				////////////////////////////////////////////////////
				// CUDA Code Path
				////////////////////////////////////////////////////
			case DeviceType::CUDA:
				Logger::Append(Logger::LogLevel::LOGLEVEL_DEBUG, "CUDA Code path - Using normal ones path");
				cuda_fill<AT>(optr, static_cast<AT>(1), numel);
				break;

			default:
				Logger::Append(Logger::LogLevel::LOGLEVEL_ERROR, "Invalid device type");
				exit(1);
			}
			
		});

		return out;
	}

