	std::ostream& operator<<(std::ostream& os, const Tensor& t) {

		dispatchAny(t.dtype(), [&](auto TA) {

			using AT = typename decltype(TA)::type;
			using RT = promote_t<AT, float>;

			Tensor tcpu = t.to(Inferno::Device::cpu());

			os << "****** Tensor \"" << tcpu.name() << "\" " << "\n";

			switch (tcpu.dtype()) {


			case DType::Int32:
				os << "dtype = Int32" << "\n";
				break;
			case DType::Float32:
				os << "dtype = Float32" << "\n";
				break;
			case DType::Float64:
				os << "dtype = Float64" << "\n";
				break;
			default:
				os << "dtype = " << static_cast<int>(tcpu.dtype()) << "\n";
				break;

			}


			os << "shape=[";
			for (size_t i = 0; i < tcpu.shape().size(); i++) {
				os << tcpu.shape()[i];
				if (i + 1 < tcpu.shape().size()) os << ", ";
			}
			os << "]\n";


			os << "strides=[";
			for (size_t i = 0; i < tcpu.shape().size(); i++) {
				os << tcpu.strides()[i];
				if (i + 1 < tcpu.strides().size()) os << ", ";
			}
			os << "]\n";

			//TODO: please print me out =(
			os << "offset=" << tcpu.offset() << "\n";


			auto p = GetImpl(tcpu);

			const AT* dptr = nullptr;
			const RT* gptr = nullptr;


			const std::vector<size_t>& shape = tcpu.shape();
			const std::vector<size_t>& strides = tcpu.strides();
			const size_t offset = tcpu.offset();


			os << "data = [";
			//Print out data
			if (p->data()) {

				size_t numdata = std::min((int)tcpu.numel(), 100);

				dptr = p->data_as_ptr<AT>();


				for (int linear = 0; linear < numdata; linear++) {


					// Convert linear index -> multidimension al index
					size_t remaining = linear;
					size_t storage_index = tcpu.offset();

					for (size_t dim = shape.size(); dim-- > 0;) {
						size_t idx_in_dim = remaining % shape[dim];
						remaining /= shape[dim];
						storage_index += idx_in_dim * strides[dim];
					}


					os << std::fixed << std::setprecision(6) << static_cast<AT>(dptr[storage_index]);
					if (linear < tcpu.numel() - 1) os << ", ";
				}
			}

			os << "]\n";

			os << "requires_grad = " << (p->requires_grad() ? "yes" : "no") << "\n";
			os << "has_grad = " << (p->grad() ? "yes" : "no") << "\n";

			//Print out grad
			os << "grad = [";

			if (p->grad()) {

				Tensor& g = *p->grad();

				dispatchAny(g.dtype(), [&](auto TG) {
					using GT = typename decltype(TG)::type;
					const GT* gptr = GetImpl(g)->data_as_ptr<GT>();
					size_t numgrad = std::min<size_t>(g.numel(), 2048);

					for (size_t linear = 0; linear < numgrad; linear++) {


						// Convert linear index -> multidimension al index
						size_t remaining = linear;
						size_t storage_index = tcpu.offset();

						for (size_t dim = shape.size(); dim-- > 0;) {
							size_t idx_in_dim = remaining % shape[dim];
							remaining /= shape[dim];
							storage_index += idx_in_dim * strides[dim];
						}


						os << std::fixed << std::setprecision(6) << gptr[storage_index];
						if (linear + 1 < numgrad) os << ", ";
					}
					});

			}
			os << "]" << "\n\n\n";

			});
		return os;
	}


