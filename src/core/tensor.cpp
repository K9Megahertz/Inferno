	Tensor Tensor::randn(Inferno::DType dtype, const std::initializer_list<size_t>& shape, const std::string& name, Inferno::Device device) {

		// Compute total number of elements			
		size_t total_size = std::accumulate(shape.begin(), shape.end(), static_cast<size_t>(1), std::multiplies<size_t>());

		switch (dtype) {
		case DType::Float32:
			return Tensor(dtype,
				Inferno::RandomGenerator::generateRandomFloatVector(total_size, -1.0f, 1.0f),
				shape,
				name,
				device);

		case DType::Float64:
			return Tensor(dtype,
				Inferno::RandomGenerator::generateRandomDoubleVector(total_size, -0.1, 0.1),
				shape,
				name,
				device);

		case DType::Int32:
			return Tensor(dtype,
				Inferno::RandomGenerator::generateRandomIntVector(total_size, -1, 1),
				shape,
				name,
				device);

		default:
			Logger::Append(Logger::LogLevel::LOGLEVEL_ERROR, "Unsupported dtype in randn");
			exit(1);
		}
	}

