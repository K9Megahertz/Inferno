    template <typename AT>
    void cuda_fill(AT* aptr, const AT value, size_t N) {

        constexpr int threads = 256;
        int blocks = static_cast<int>((N + threads - 1) / threads); 
        cuda_fill_kernel << <blocks, threads >> > (aptr, value, N);
    }


