    template <typename AT>
    __global__ void cuda_fill_kernel(AT* data, const AT value, size_t n)
    {
        size_t i = blockIdx.x * blockDim.x + threadIdx.x;
        if (i < n)
            data[i] = value;
    }


    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
