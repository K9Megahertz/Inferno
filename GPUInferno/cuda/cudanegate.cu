    template<typename AT>
    __global__ void negate_kernel(const AT* aptr, AT* outptr, size_t N) {
        size_t i = blockIdx.x * blockDim.x + threadIdx.x;
        if (i < N) outptr[i] = static_cast<AT>(-aptr[i]);
    }


    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //
    //  Function name
