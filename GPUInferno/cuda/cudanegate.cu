    template<typename AT>
    void cuda_negate(const AT* aptr, AT* outptr, size_t N) {
        dim3 block(256);
        dim3 grid((N + block.x - 1) / block.x);
        negate_kernel << <grid, block >> > (aptr, outptr, N);
    }


