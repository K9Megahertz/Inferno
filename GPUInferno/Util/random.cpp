// MathLib.cpp
#include "random.h"
#include <stdexcept>

// Initialize static members
std::mt19937 Inferno::RandomGenerator::gen;
bool Inferno::RandomGenerator::isInitialized = false;

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function name
//
//
//
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void Inferno::RandomGenerator::initialize() {
    // Seed the generator with a random device for non-deterministic results
    std::random_device rd;
    gen.seed(rd());
    isInitialized = true;
}


//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function name
//
//
//
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void Inferno::RandomGenerator::initializeWithSeed(unsigned int seed) {
    // Seed the generator with a specific value for deterministic results
    gen.seed(seed);
    isInitialized = true;
}


//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function name
//
//
//
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

std::vector<float> Inferno::RandomGenerator::generateRandomFloatVector(int size, float minValue, float maxValue) {
    // Check if the generator has been initialized
    if (!isInitialized) {
        throw std::runtime_error("Random generator not initialized. Call initialize() or initializeWithSeed() first.");
    }

    std::vector<float> randomValues;
    randomValues.reserve(size);

    std::uniform_real_distribution<float> distribution(minValue, maxValue);

    for (int i = 0; i < size; i++) {
        randomValues.push_back(distribution(gen));
    }

    return randomValues;
}



