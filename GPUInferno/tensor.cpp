	Tensor Tensor::ones_like(const Tensor& A)
	{
		return dispatchOne(A.dtype(), [&](auto T) {
			using AT = typename decltype(T)::type;

			Tensor out(A.dtype(), A.shape(), "ones_like", A.device());

			auto ptr = GetImpl(out)->data_as_ptr<AT>();
			size_t n = GetImpl(out)->numel();



			switch (A.device().m_type) {

				////////////////////////////////////////////////////
				// CPU Code Path
				////////////////////////////////////////////////////
			case DeviceType::CPU:
				Logger::Append(Logger::LogLevel::LOGLEVEL_DEBUG, "CPU Code path");
				for (size_t i = 0; i < n; i++)
					ptr[i] = static_cast<AT>(1);
				break;

				////////////////////////////////////////////////////
				// CUDA Code Path
				////////////////////////////////////////////////////
			case DeviceType::CUDA:
				Logger::Append(Logger::LogLevel::LOGLEVEL_DEBUG, "CUDA Code path");
				cuda_fill<AT>(ptr, 1, n);
				break;

			default:
				Logger::Append(Logger::LogLevel::LOGLEVEL_ERROR, "Invalid device type");
				exit(1);
			}

			return out;
			});
	}


